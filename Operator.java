public class Operator {
    public static void main(String[] args) {
        int hasil = 3 + 2;
        System.out.println("1+1=" + hasil);

        hasil = hasil - 1;
        System.out.println("hasilnya sekarang " + hasil);

        hasil = hasil * 3;
        System.out.println(hasil);

        hasil = hasil / 2;
        System.out.println(hasil);

        hasil += 10;
        System.out.println(hasil);

        hasil -= 5;
        System.out.println(hasil);

        hasil *= 20;
        System.out.println(hasil);
    }
}
